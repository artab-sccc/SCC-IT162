        <h2>Responsive Design vs Mobile Apps</h2>

        <p>
            With the ever-so changing landscape of the world’s technology, there have been many new ways to access the internet for information. Almost everyone has now a smartphone, tablet, desktop or some computing device that gives them access to various internet resources on the web. However, with a lot of these devices taking on varying sizes and mobility, the information displayed on each must be taken into consideration. For example, a website on a smartphone will need to display bigger text as compared to accessing the website on a desktop machine. How do websites accommodate the differences between mobile and desktop devices in order to make their content viewable and accessible on both platforms?
        </p>

