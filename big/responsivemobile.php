            There are two main ways a website can display their content on both devices: having a responsive website vs a mobile app. The two both have their pros and cons, but depending on the website’s need for its online presence, will it benefit from one or the other.
        </p>

        <p>
            A responsive website is great for sites that need to create their online presence quickly. Since it is fast and cheaper to develop, it makes it more simpler for users to access the information from any device. However, it’s functionality may not be as efficient as it may not take advantage of mobile device features, such as the camera or GPS function.
        </p>

        <p>
            On the other hand, a mobile app are great for websites that are looking to take advantage of those features to make a user’s experience catered to them. <a href="https://rubygarage.org/blog/mobile-app-vs-mobile-website">Source</a> - “Mobile apps provide better user experiences, load content faster, and are easier to use.” With a mobile app users can get notifications, use the GPS function or work offline in the app. It is great for sites that are looking for interaction with the user.
        </p>

        <p>
            As both responsive design and mobile apps look to accommodate the various devices, both are very different in how they provide the experience to the user. Depending on the website’s needs for their users, responsive or mobile can both have their pros and cons.
